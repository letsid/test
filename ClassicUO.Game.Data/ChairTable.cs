	public static void Load()
	{
		string text = Path.Combine(CUOEnviroment.ExecutablePath, "Data", "Client");
		if (!Directory.Exists(text))
		{
			Directory.CreateDirectory(text);
		}
		string text2 = Path.Combine(text, "chair.txt");
		FileInfo fileInfo = new FileInfo(text2);
		List<AnimationsLoader.SittingInfoData> list = new List<AnimationsLoader.SittingInfoData>(_alathairChairTable);
		AnimationsLoader.SittingInfoData[] defaultTable = _defaultTable;
		for (int i = 0; i < defaultTable.Length; i++)
		{
			AnimationsLoader.SittingInfoData item = defaultTable[i];
			if (!list.Any((AnimationsLoader.SittingInfoData x) => x.Graphic == item.Graphic))
			{
				list.Add(item);
			}
		}
		if (!File.Exists(text2))
		{
			File.Create(text2).Close();
		}
		using (StreamWriter streamWriter = new StreamWriter(fileInfo.Open(FileMode.Truncate)))
		{
			foreach (AnimationsLoader.SittingInfoData item2 in list)
			{
				streamWriter.WriteLine($"{item2.Graphic},{item2.Direction1},{item2.Direction2},{item2.Direction3},{item2.Direction4},{item2.OffsetY},{item2.MirrorOffsetY}");
			}
		}
		TextFileParser textFileParser = new TextFileParser(File.ReadAllText(text2), new char[3] { ' ', '\t', ',' }, new char[2] { '#', ';' }, new char[2] { '"', '"' });
		while (!textFileParser.IsEOF())
		{
			List<string> list2 = textFileParser.ReadTokens();
			if (list2 != null && list2.Count >= 7)
			{
				ushort.TryParse(list2[0], out var result);
				sbyte.TryParse(list2[1], out var result2);
				sbyte.TryParse(list2[2], out var result3);
				sbyte.TryParse(list2[3], out var result4);
				sbyte.TryParse(list2[4], out var result5);
				sbyte.TryParse(list2[5], out var result6);
				sbyte.TryParse(list2[6], out var result7);
				Table.Add(result, new AnimationsLoader.SittingInfoData(result, result2, result3, result4, result5, result6, result7, drawback: false));
			}
		}
	}
